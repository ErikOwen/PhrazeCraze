package bev.and.owe;

public class PhrazePack {
	private String answer;
	private String phraze;
	
	public PhrazePack(String answer, String phraze) {
		this.answer = answer;
		this.phraze = phraze;
	}
	
	public String getAnswer() {
		return this.answer;
	}
	
	public String getPhraze() {
		return this.phraze;
	}
}
